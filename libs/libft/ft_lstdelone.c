#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void*))
{
	if (lst == NULL)
		return ;
	if (lst->key != NULL)
		del(lst->key);
	if (lst->value != NULL)
		del(lst->value);
	free(lst);
}
